//Basic functions in Javascript

// length method
let name = "Samyak Sukdeve  ";
console.log(name.length);

//indexOf method
console.log(name.indexOf("yak"));

//slice method
console.log(name.slice(0, 5));

//replace method
console.log(name.replace("Sukdeve", "is lucky"));

//split method
console.log(name.split(""));

//trim method
console.log(name.trim());

//toLowerCase and toUpperCase method
console.log(name.toLowerCase());
console.log(name.toUpperCase());

